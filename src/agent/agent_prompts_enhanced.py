def get_prompt_by_role(role: str) -> str:
    """
    根据Agent角色获取对应的提示词
    
    Args:
        role: Agent角色标识
    
    Returns:
        对应的提示词字符串
    """
    prompts = {
        # Analyst Team
        'fundamentals_analyst': FUNDAMENTALS_ANALYST_PROMPT,
        'sentiment_analyst': SENTIMENT_ANALYST_PROMPT,
        'news_analyst': NEWS_ANALYST_PROMPT,
        'technical_analyst': TECHNICAL_ANALYST_PROMPT,
        'quant_analyst': QUANT_ANALYST_PROMPT,  # NEW
        
        # Researcher Team
        'bullish_researcher': BULLISH_RESEARCHER_PROMPT,
        'bearish_researcher': BEARISH_RESEARCHER_PROMPT,
        
        # Decision Layer
        'trader': TRADER_PROMPT,
        'risk_manager_aggressive': RISK_MANAGER_AGGRESSIVE_PROMPT,
        'risk_manager_neutral': RISK_MANAGER_NEUTRAL_PROMPT,
        'risk_manager_conservative': RISK_MANAGER_CONSERVATIVE_PROMPT,
        'portfolio_manager': PORTFOLIO_MANAGER_PROMPT,
        
        # Moderator
        'debate_moderator': DEBATE_MODERATOR_PROMPT,
        
        # 向后兼容: 保留旧的角色名称
        'data_analyst': TECHNICAL_ANALYST_PROMPT,
        'news_researcher': NEWS_ANALYST_PROMPT,
        'bull_reviewer': BULLISH_RESEARCHER_PROMPT,
        'bear_reviewer': BEARISH_RESEARCHER_PROMPT,
    }
    
    return prompts.get(role, TECHNICAL_ANALYST_PROMPT)

